export function correctMonth(month:string) {

    if(month === 'Январь') return 'Января'
    if(month === 'Февраль') return 'Февраля'
    if(month === 'Март') return 'Марта'
    if(month === 'Апрель') return 'Апреля'
    if(month === 'Май') return 'Мая'
    if(month === 'Июнь') return 'Июня'
    if(month === 'Июль') return 'Июля'
    if(month === 'Август') return 'Августа'
    if(month === 'Сентябрь') return 'Сентября'
    if(month === 'Октябрь') return 'Октября'
    if(month === 'Ноябрь') return 'Ноября'
    if(month === 'Декабрь') return 'Декабря'
}